function setLang(lang) {
  if (!TRANSLATIONS[lang]) lang = 'ru';

  /* highlight active lang button */
  document.querySelectorAll('.lang-btn[data-lang]').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  var dict = TRANSLATIONS[lang];

  /* translate [data-i18n] elements */
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var val = dict[key];
    if (val === undefined) return;

    /* if the element has child elements (e.g. nav link with arrow span),
       only update the first text node so children are preserved */
    if (el.children.length > 0) {
      for (var i = 0; i < el.childNodes.length; i++) {
        if (el.childNodes[i].nodeType === 3) {   /* TEXT_NODE */
          el.childNodes[i].nodeValue = val + ' ';
          break;
        }
      }
    } else {
      el.textContent = val;
    }
  });

  /* translate placeholders [data-i18n-ph] */
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-ph');
    var val = dict[key];
    if (val !== undefined) el.placeholder = val;
  });

  try { localStorage.setItem('itcollege_lang', lang); } catch(e) {}
}

/* auto-apply saved language on page load */
(function() {
  try {
    var saved = localStorage.getItem('itcollege_lang');
    if (saved && TRANSLATIONS[saved]) setLang(saved);
  } catch(e) {}
})();
